package JavaConcepts;

public class whileDoWhile {
    public static void main(String[] args){
        int i =0,j=0;
        while(i<5){
            System.out.println(i);
            i++;
        }
        System.out.println("========");
        do{
            System.out.println(j);
            j++;
        }while(j>5);
    }
}
