package JavaConcepts;

public class forLoop {
    public static void main(String[] args){
        for(int i =0;i<5;i++){
            System.out.println(i);
        }
        System.out.println("=======");
        for(int i = 5; i>0; i --){
            System.out.println(i);
        }
        System.out.println("=======");
        for(float i =5f;i>0;i=i/2){
            System.out.println(i);
        }
    }
}
